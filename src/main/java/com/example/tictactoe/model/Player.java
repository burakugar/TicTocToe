package com.example.tictactoe.model;

/**
 * Represents the players in a Tic-Tac-Toe game.
 * This enum defines the possible states of a cell on the game board.
 */
public enum Player {
    /**
     * Represents player X.
     */
    X,

    /**
     * Represents player O.
     */
    O,

    /**
     * Represents an empty cell on the board.
     */
    EMPTY
}
